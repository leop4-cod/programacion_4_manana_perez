fun main() {

    println("Operadores de comparacion")
    val numero1 = 10
    val numero2 = 3

    println("Igualdad")
    println("$numero1 == $numero2: ${numero1 == numero2}")

    println("Desigualdad")
    println("$numero1 != $numero2: ${numero1 != numero2}")

    println("Mayor que")
    println("$numero1 > $numero2: ${numero1 > numero2}")

    println("Menor que")
    println("$numero1 < $numero2: ${numero1 < numero2}")

    println("Mayor o igual que")
    println("$numero1 >= $numero2: ${numero1 >= numero2}")

    println("Menor o igual que")
    println("$numero1 <= $numero2: ${numero1 <= numero2}")
}