fun main() {

    println("Operadores de comparacion - Funeraria")
    val serviciosHoy = 10
    val salasDisponibles = 3

    println("Igualdad")
    println("$serviciosHoy == $salasDisponibles: ${serviciosHoy == salasDisponibles}")

    println("Desigualdad")
    println("$serviciosHoy != $salasDisponibles: ${serviciosHoy != salasDisponibles}")

    println("Mayor que")
    println("$serviciosHoy > $salasDisponibles: ${serviciosHoy > salasDisponibles}")

    println("Menor que")
    println("$serviciosHoy < $salasDisponibles: ${serviciosHoy < salasDisponibles}")

    println("Mayor o igual que")
    println("$serviciosHoy >= $salasDisponibles: ${serviciosHoy >= salasDisponibles}")

    println("Menor o igual que")
    println("$serviciosHoy <= $salasDisponibles: ${serviciosHoy <= salasDisponibles}")
}